package CoreJava;

public class RelationalOperator {
    public static void main(String[] args) {

        int a=20;
        int b=30;
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a != b);

        System.out.println(a = b); // It is assigning the value
        System.out.println(a == b); // It will compare the value
    }
}
