package CoreJava.loop;

public class ForLoop2 {
    public static void main(String[] args) {
        //Table of 9
        // 9*1=9

        int n =9;
        for (int i=1; i <= 10; i++ ){
            System.out.println(n + "*" + i + "=" + n*i);
        }

        for (;;) {
            System.out.println("Priti");
            
        }


    }
}
