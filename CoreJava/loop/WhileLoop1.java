package CoreJava.loop;

public class WhileLoop1 {
    public static void main(String[] args) {
        //While Loop
        //Initialization will be outside the code
        //Condition and Updation will be inside the code

        //Initialization
        int i=10;
        while (i <= 10){ //Exit Condition
            //code
            System.out.println(i);
            //Increment or Updation
            i++;

        }


    }
}
