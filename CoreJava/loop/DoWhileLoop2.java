package CoreJava.loop;

public class DoWhileLoop2 {
    public static void main(String[] args) {
        System.out.println("Start of the Loop");
       int a=0;
        do {
            System.out.println("Value of a -> " + a);
            a--;
        }while (a>-5);


        int b=1;
        do {
            System.out.println(b);
            b++;
        }while (b<=10);


    }





}
