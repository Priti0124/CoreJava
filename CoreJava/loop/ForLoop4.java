package CoreJava.loop;

public class ForLoop4 {
    public static void main(String[] args) {

        // Nested For Loop
        // These are used for Star Pattern

        for (int i = 0; i < 3; i++) {
            System.out.println(i);
            for (int j = 0; j < 5; j++) {
                System.out.println(j);

            }
            
        }
        // i= 0, j= 0,1,2,3,4
        // i= 1, j= 0,1,2,3,4
        // i= 2, j= 0,1,2,3,4

    }
}
